package com.changhong.tvos.dtv.epg.normal;

public class Week {

	private static final long serialVersionUID = 1L;

	private String week;
	private String day;

	public Week() {

	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}
}
